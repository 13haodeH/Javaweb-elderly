package com.elderly.entity;

public class homer_user {
	private String HOMER_NAME ;
	private String HOMER_PHONE ;
	private String HOMER_TYPE;
	private String HOMER_EMAIL;
	private String HOMER_MASSAGE;
	public homer_user(String hOMER_NAME, String hOMER_PHONE, String hOMER_TYPE, String hOMER_EMAIL,
			String hOMER_MASSAGE) {
		super();
		HOMER_NAME = hOMER_NAME;
		HOMER_PHONE = hOMER_PHONE;
		HOMER_TYPE = hOMER_TYPE;
		HOMER_EMAIL = hOMER_EMAIL;
		HOMER_MASSAGE = hOMER_MASSAGE;
	}
	public String getHOMER_NAME() {
		return HOMER_NAME;
	}
	public void setHOMER_NAME(String hOMER_NAME) {
		HOMER_NAME = hOMER_NAME;
	}
	public String getHOMER_PHONE() {
		return HOMER_PHONE;
	}
	public void setHOMER_PHONE(String hOMER_PHONE) {
		HOMER_PHONE = hOMER_PHONE;
	}
	public String getHOMER_TYPE() {
		return HOMER_TYPE;
	}
	public void setHOMER_TYPE(String hOMER_TYPE) {
		HOMER_TYPE = hOMER_TYPE;
	}
	public String getHOMER_EMAIL() {
		return HOMER_EMAIL;
	}
	public void setHOMER_EMAIL(String hOMER_EMAIL) {
		HOMER_EMAIL = hOMER_EMAIL;
	}
	public String getHOMER_MASSAGE() {
		return HOMER_MASSAGE;
	}
	public void setHOMER_MASSAGE(String hOMER_MASSAGE) {
		HOMER_MASSAGE = hOMER_MASSAGE;
	}
	
	
	

	
	
	
}
