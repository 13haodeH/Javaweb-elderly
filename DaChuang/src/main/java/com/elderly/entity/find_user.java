package com.elderly.entity;

public class find_user {
			private String FIND_NAME;
			private String FIND_AGE;
			private String FIND_PHONE;
			private String FIND_POSITION;
			private String FIND_FEATURES;
			public find_user(String fIND_NAME, String fIND_AGE, String fIND_PHONE, String fIND_POSITION,
					String fIND_FEATURES) {
				super();
				FIND_NAME = fIND_NAME;
				FIND_AGE = fIND_AGE;
				FIND_PHONE = fIND_PHONE;
				FIND_POSITION = fIND_POSITION;
				FIND_FEATURES = fIND_FEATURES;
			}
			public String getFIND_NAME() {
				return FIND_NAME;
			}
			public void setFIND_NAME(String fIND_NAME) {
				FIND_NAME = fIND_NAME;
			}
			public String getFIND_AGE() {
				return FIND_AGE;
			}
			public void setFIND_AGE(String fIND_AGE) {
				FIND_AGE = fIND_AGE;
			}
			public String getFIND_PHONE() {
				return FIND_PHONE;
			}
			public void setFIND_PHONE(String fIND_PHONE) {
				FIND_PHONE = fIND_PHONE;
			}
			public String getFIND_POSITION() {
				return FIND_POSITION;
			}
			public void setFIND_POSITION(String fIND_POSITION) {
				FIND_POSITION = fIND_POSITION;
			}
			public String getFIND_FEATURES() {
				return FIND_FEATURES;
			}
			public void setFIND_FEATURES(String fIND_FEATURES) {
				FIND_FEATURES = fIND_FEATURES;
			}
			
			
			
}
